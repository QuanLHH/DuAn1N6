    public void ProgressBar() {
        new Thread() {
            @Override
            public void run() {
                while (true) {
                    int value = pb_values.getValue() + 1;
                    pb_values.setValue(value);
                    pb_values.setStringPainted(true);
                    if (value == 100) {
                        dispose();
                    }
                    try {
                        Thread.sleep(20);
                    } catch (Exception e) {
                        e.printStackTrace();
                    } 
                }
            }
        }.start();
    }
